/*
 * sobel directional operator based edge detection
 */
int S1[3][3] = { { 1,  0, -1}, {2, 0, -2}, {1, 0, -1} };
int S2[3][3] = { {-1, -2, -1}, {0, 0,  0}, {1, 2,  1} };
char pixBuf1[128];
char pixBuf2[128];
char pixBuf3[128];
int edgeDetect () {

	int           i, j, n1, n2,
				  iWid, iLen,
	              k1, k2;
	char          ch;
	unsigned char **w;
	unsigned char *temp, tmp;
	int           y1,y2;
	unsigned int  zn2;

	//get image width and length
	iWid = BUFDIM;
	iLen = BUFDIM;

	//image transfer buffer (tailored for malloc)
    //w = (unsigned char **)malloc(3*4); //3=sizeof(char *));
    //for (i=0; i<3; i++) *(w+i) = (unsigned char *)malloc(iWid); //calloc(iWid, sizeof(char));

	int buf[3];
	w=buf;
	*(w+0) = pixBuf1;
	*(w+1) = pixBuf2;
	*(w+2) = pixBuf3;
	
	//the algo
	int numCharIn  = 0;   //idx for first input char
	int numCharOut = 0;   //idx for first output char
	for(n1=0; n1<iLen; n1++){

		//transfer row n2 to the last row of w
		for (j=0; j<iWid; j++) {

			//ch = (fgetc(fp));
			ch = ibuf[numCharIn++];
			*((*(w+2))+j) = (unsigned char)ch;
			//printf("one char in row: %c\n",ch);
		}

		//for-1
		for (n2=0; n2<iWid; n2++) {
			y1=0;
			y2=0;

			//for-2
			for (k1=-1; k1<=1; k1++) {

				//for-3
				for (k2=-1; k2<=1; k2++) {
					if ( (n2+k2)<0 || (n2+k2-1)>iWid ) {
						continue;
					}
					tmp = ( * ( (*(w+1+k1)) + (n2+k2-1) ) );
					y1 += S1[1+k1][1+k2]*tmp;
					y2 += S2[1+k1][1+k2]*tmp;
				}//for-3
			}//for-2

			y1  = y1>0 ? y1 : -y1; // abs(y1);
			y2  = y2>0 ? y2 : -y2; // abs(y2);
			zn2 = (y1 > y2) ? y1 : y2;

			//printf("writing one char out: %c\n",zn2);
			obuf[numCharOut++]=zn2;

		} //for1

		//shift rows of w
		temp   = *w;
		*(w)   = *(w+1);
		*(w+1) = *(w+2);
		*(w+2) = temp;
	}//for-algo

    return 0;
}

