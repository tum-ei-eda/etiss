def print_logo() -> str:
    output = ""
    logo_height = len(logo_chars['v'])
    title = "verificator"
    for i in range(logo_height):
        for c in title:
            if c in logo_chars:
                output += f"{logo_chars[c][i]}"
        output += logo_chars['copyright'][i]
        output += '\n'
    return output



